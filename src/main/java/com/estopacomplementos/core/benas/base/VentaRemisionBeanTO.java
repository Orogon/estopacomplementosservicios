package com.estopacomplementos.core.benas.base;

public class VentaRemisionBeanTO {
	
	private String codigoProducto;
	private String cantidadProducto;
	private String descripcion;
	private String precioVenta;	
	private String descuento;
	private String precioDesc;
	private String ahorroTotal;
	private String importe;
	
	public String getCodigoProducto() {
		return codigoProducto;
	}
	public void setCodigoProducto(String codigoProducto) {
		this.codigoProducto = codigoProducto;
	}
	public String getCantidadProducto() {
		return cantidadProducto;
	}
	public void setCantidadProducto(String cantidadProducto) {
		this.cantidadProducto = cantidadProducto;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getPrecioVenta() {
		return precioVenta;
	}
	public void setPrecioVenta(String precioVenta) {
		this.precioVenta = precioVenta;
	}
	public String getDescuento() {
		return descuento;
	}
	public void setDescuento(String descuento) {
		this.descuento = descuento;
	}
	public String getPrecioDesc() {
		return precioDesc;
	}
	public void setPrecioDesc(String precioDesc) {
		this.precioDesc = precioDesc;
	}
	public String getAhorroTotal() {
		return ahorroTotal;
	}
	public void setAhorroTotal(String ahorroTotal) {
		this.ahorroTotal = ahorroTotal;
	}
	public String getImporte() {
		return importe;
	}
	public void setImporte(String importe) {
		this.importe = importe;
	}	
	
}
